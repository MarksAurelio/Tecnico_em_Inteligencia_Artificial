op = input('Digite um mês do ano: ')
match op:
    case 1:
        print('Jan')
    case 2:
        print('Fev')
    case 3:
        print('Mar')
    case 4:
        print('Abr')
    case 5:
        print('Mai')
    case 6:
        print('Jun')
    case 7:
        print('Jul')
    case 8:
        print('Ago')
    case 9:
        print('Set')
    case 10:
        print('Out')
    case 11:
        print('Nov')
    case 12:
        print('Dez') 
    case _:
        print('Opção inválida')

