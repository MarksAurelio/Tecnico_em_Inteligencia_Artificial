# O que é uma classe
# Planta de um prédio, um projeto arquitetônico
# Modelo para criar objetos
# A classe é um conjunto de objetos
class Personagem:
    # Dentro de uma classe dfinimos atributos e métodos
    # idade, nome, nome social, altura, peso 
    def __init__(self, nome, idade, altura, peso, cor):
        self.nome = nome
        self.idade = idade
        self.altura = altura
        self.peso = peso
        self.cor = cor
   
